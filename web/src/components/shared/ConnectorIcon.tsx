const connectorConfig = {
  spotify: { label: "Spotify", colorClass: "text-spotify", Logo: SpotifyLogo },
  lastfm: { label: "Last.fm", colorClass: "text-lastfm", Logo: LastfmLogo },
  apple: {
    label: "Apple Music",
    colorClass: "text-apple",
    Logo: AppleMusicLogo,
  },
  musicbrainz: {
    label: "MusicBrainz",
    colorClass: "text-musicbrainz",
    Logo: MusicBrainzLogo,
  },
} as const;

export type ConnectorName = keyof typeof connectorConfig;

/** Get the display label for a connector name (e.g. "spotify" → "Spotify"). */
export function getConnectorLabel(name: string): string {
  const config = connectorConfig[name as ConnectorName];
  if (config) return config.label;
  // Backend uses "apple_music" but icon config uses "apple"
  if (name === "apple_music") return "Apple Music";
  return name;
}

const iconSizes = {
  sm: "h-4 w-4",
  lg: "h-8 w-8",
} as const;

interface ConnectorIconProps {
  name: string;
  className?: string;
  /** Icon size — "sm" (16px, default) or "lg" (32px). */
  iconSize?: keyof typeof iconSizes;
  /** Hide the text label — show icon only. */
  labelHidden?: boolean;
}

export function ConnectorIcon({
  name,
  className = "",
  iconSize = "sm",
  labelHidden = false,
}: ConnectorIconProps) {
  const config = connectorConfig[name as ConnectorName];
  if (!config) return null;

  const { Logo } = config;

  return (
    <span
      className={`inline-flex items-center gap-2 font-display text-sm font-medium ${config.colorClass} ${className}`}
      {...(labelHidden ? { "aria-hidden": true } : { title: config.label })}
    >
      <Logo className={`${iconSizes[iconSize]} shrink-0`} />
      {!labelHidden && config.label}
    </span>
  );
}
